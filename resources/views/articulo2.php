    <div class="article-title">La Depresión</div>
    <img src="../resources/images/depresion.jpg" alt="Imagen sobre depresión">

    <p>La depresión es un trastorno del estado de ánimo que afecta cómo te sientes, piensas y manejas las actividades diarias. Puede causar una variedad de síntomas, desde cambios en el apetito hasta sentimientos de desesperanza y falta de energía.</p>

    <p>Las personas con depresión pueden experimentar episodios prolongados de tristeza y pérdida de interés en actividades que antes disfrutaban. Es una condición médica seria que puede afectar la calidad de vida y, en casos severos, llevar a pensamientos suicidas.</p>

    <p>El tratamiento para la depresión puede incluir terapia, medicamentos y cambios en el estilo de vida. La comprensión y el apoyo emocional son fundamentales para quienes enfrentan este trastorno.</p>
