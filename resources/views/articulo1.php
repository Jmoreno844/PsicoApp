    <img src="../resources/images/ansiedad.jpg" alt="Imagen sobre ansiedad">

    <p>La ansiedad es un trastorno mental caracterizado por sentimientos de preocupación, nerviosismo o miedo. Estos sentimientos pueden ser desproporcionados con la situación y afectar la vida diaria de una persona.</p>

    <p>Las manifestaciones de la ansiedad pueden incluir síntomas físicos como palpitaciones, sudoración y temblores, así como preocupaciones persistentes, dificultades para concentrarse y problemas para conciliar el sueño.</p>

    <p>El tratamiento de la ansiedad a menudo implica terapia, cambios en el estilo de vida y, en algunos casos, medicamentos. La comprensión y el apoyo social son esenciales para aquellos que enfrentan este trastorno.</p>
