    <p>La esquizofrenia es un trastorno mental que afecta la forma en que una persona piensa, siente y se comporta. Aunque los síntomas pueden variar, algunas características comunes incluyen alucinaciones, delirios y dificultades para pensar con claridad.</p>

    <p>Es importante destacar que la esquizofrenia no significa tener múltiples personalidades, como a veces se retrata de manera incorrecta en los medios. En cambio, los individuos con esquizofrenia pueden experimentar una variedad de síntomas que afectan su capacidad para funcionar en la vida diaria.</p>

    <p>El tratamiento de la esquizofrenia generalmente implica medicamentos, terapia y apoyo continuo. La comprensión y el apoyo de amigos y familiares son cruciales en el manejo de esta enfermedad.</p>
